        <H3>Para esto se debe considerar el cliente centraliza varias aplicaciones y recursos en el servidor. El servidor a su vez asegura que estos recursos estén disponibles siempre que el cliente los necesite.</H3>
        <center><img src="https://st2.depositphotos.com/5989012/12368/v/950/depositphotos_123688204-stock-illustration-client-server-model-concept.jpg" alt="" height="350" width="700"></center>
