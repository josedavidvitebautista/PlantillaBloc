<section id="#2">
    <header class="major">
        <h2>Evolución De Los Sistemas De Información</h2>
    </header>
    <div>
        <center><img src="https://i.ytimg.com/vi/KVv5PyTo49Q/maxresdefault.jpg" alt="" height="450" width="750"></center>
        <H3>1950 - 1960: Procesamiento de datos electrónicos, sistema de procesamiento de transacciones. <br>Principalmente llevar a cabo actividades como transacción, grabación y procesamiento de contabilidad. Si se usa principalmente para manejar los datos electrónicos.</H3>
        <h3>1960 - 1970: Sistemas de información gerencial.<br>El SI ha evolucionado de TPS Sistema de información de gestión (MSI) procesando los datos en informes útil e informativo para organizar, evaluar y administrar de manera efectiva.<br>MSI tiene tres tipos de información básica.</h3>
        <h4>
            <li>Un informe detallado de control.</li>
        </h4>
        <h4>
            <li>Un Información resumida determinando datos en un formato separado.</li>
        </h4>
        <h4>
            <li>La información mediante el filtrado especializada en el ahorrar tiempo.</li>
        </h4>
        <h3>1970 a 1980: Sistemas de soporte de decisiones<br>La función de SI está ligada a la gestión más que a un objetivo técnico en una organización. Se centra en los "sistemas interactivos basados en computadora" para ayudar a la toma de decisiones a resolver problemas a este proceso se le denomina Sistemas de Soporte de Decisiones (DSS) el cual planifica, gestiona y opera los datos de fuentes internas y / o externas.
        </h3>
        <h3>1980 a 1990: Sistemas de información ejecutivo. <br> Se desarrollaron sistemas de información ejecutivo (ESI) o sistemas de soporte ejecutivo (ESS) los cuales ofrecen servicios de toma de decisiones a través de información interna y externa con la cual cumplir con el o los objetivos.</h3>
        <h3>1990 a 2000 Se incorpora los sistemas expertos (ES) y sistemas de gestión del conocimiento (KMS) interconectados entre sí. Donde los ES son un SI con el potencial de tomar decisiones casi como un humano, mientras que el KMS se encarga de recuperar y almacenar información con el fin de expandir el conocimiento para la mejora en la organización y creación.</h3>
        <h3>2000 - presente: E-Business.<br> Las funciones de los sistemas de información de este período siguen siendo las mismas que las de hace 50 años: mantenimiento de registros, gestión de informes, procesamiento de transacciones, soporte a los procesos de gestión y organización de la gestión. Se utiliza para respaldar los procesos comerciales, la toma de decisiones y la ventaja competitiva.</h3><br>
